// --- Operadores Aritiméticos ---

const num1 = 10
const num2 = 3
const num3 ='10'
const num4 = 5
const num5 = 'Lucas'
const num6 = '5.2'

// + Adição / Concatenação
console.log(`Soma ${num1} + ${num2} =`, num1 + num2)
console.log(`Concatenação ${num1} + ${num3} =`, num1 + num3)

// - Subtração
console.log(`Subtração ${num1} - ${num2} =`, num1 - num2)

// * Multiplicação
console.log(`Multiplicação ${num1} * ${num2} =`, num1 * num2)

// / Divisão
console.log(`Divisão ${num1} / ${num2} =`, num1 / num2)

// ** Potenciação
console.log(`Potenciação ${num1} ** ${num2} =`, num1 ** num2)

// % sobra da divisão
console.log(`Sobra da Divisão ${num1} % ${num2} =`, num1 % num2)

// Constas multiplas
console.log(`-> ${num1} + ${num2} * ${num4} =`, num1 + num2 * num4)
console.log(`-> (${num1} + ${num2}) * ${num4} =`, (num1 + num2) * num4)

// Operador Incremento
let contadorIncremento = 4
contadorIncremento++
contadorIncremento++
console.log(contadorIncremento)

// Operador Decremento
let contadorDecremento = 10
contadorDecremento--
contadorDecremento--
console.log(contadorDecremento)