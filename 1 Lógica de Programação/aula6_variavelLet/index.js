// ### DICAS VARIÁVEIS ###

// - Não podemos criar variáveis com palavras reservadas, exemplo - let If / let console

// - Variáves precisam ter nomes significativos, exemplo - let nome / let number

// - Não pode começar o nome de uma variável com um número, exemplo - let 1nome / let 2pessoa

// - As variáveis não podem conter espaços ou traços, exemplo - let nome completo / let nome-cliente
// -- para nome compostos de variáveis o correto é utilizar camelCase, exemplo abaixo:
let nomeCompletoDoCliente = 'José dos Palmares'
console.log(nomeCompletoDoCliente)
console.log('')

let name = 'Otávio'
console.log(name, 'nasceu em 1984.')
console.log('Em 2000', name, 'conheceu Maria.')
console.log(name, 'casou-se com Maruia em 2012.')
console.log('Maria teve 1 filho com', name, 'em 2015.')
console.log('O filho de', name, 'se chama Eduardo.')
console.log('')

let nome //declarou a variável
nome = 'João Silva' // inicializando a variável
console.log(nome)
nome = 'Otávio Pires'
console.log(nome)


